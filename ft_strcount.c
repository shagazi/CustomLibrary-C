#include "libft.h"

int		ft_strcount(char *s, char c)
{
	int		i;
	int		k;
	char	*str;

	k = 0;
	i = 0;
	while (s[i] != '\0' || s[i] == c)
	{
		if (s[i] != c)
		{
			while (s[i] && s[i] != c)
			{
				i++;
				k++;
			}
			i -= k;
			k += i - 1;
			while (i <= k)
				s[i++] = c;
			return (1);
		}
		i++;
	}
	return (0);
}
