#include "libft.h"

char	*ft_word(char *s)
{
	int		i;
	int		k;
	char	*str;

	i = 0;
	k = 0;
	while (s[i] != '\0')
	{
		if ((s[i] >= 65 && s[i] <= 90) || (s[i] >= 97 && s[i] <= 122))
		{
			while ((s[i] >= 65 && s[i] <= 90) || (s[i] >= 97 && s[i] <= 122))
			{
				i++;
				k++;
			}
			i -= k;
			str = ft_strsub(s, i, k);
			k += i - 1;
			while (i <= k)
				s[i++] = ' ';
			return (str);
		}
		i++;
	}
	return (0);
}
