#include "libft.h"

void 	*ft_memset(void *b, int c, size_t len)
{
	int i;
	unsigned char *str = b;

	i = 0;
	while(str[i] != '\0' && i < len)
	{
		str[i] = c;
		i++;
	}
	return (b);
}
