#include "libft.h"

void *ft_memcpy(void *s1, const void *s2, size_t n)
{
	int i; 
	char *a1 = s1;
	const char *b1 = s2;

	i = 0;
	while (s1 != '\0' && i < n)
	{
		a1[i] = b1[i];
		i++;

	}
	return (a1);
}

